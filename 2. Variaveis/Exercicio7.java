public class Exercicio7 {
    public static void main(String[] args) throws Exception {
        String a = "Bom dia";
        System.out.println("\"Bom dia\": String (" + a + ")");

        byte b = 3;
        System.out.println("3: Byte (" + b + ")");

        double c = 235.13;
        System.out.println("235.13: Double (" + c + ")");

        boolean d = true;
        System.out.println("true: Boolean (" + d + ")");

        short e = -135;
        System.out.println("-135: Short (" + e + ")");

        float f = 256.23F;
        System.out.println("256.23F: Float (" + f+ ")");

        char g = 'A';
        System.out.println("\'A\': Char (" + g + ")");

        long h = 6463275245745L;
        System.out.println("6463275245745L: Long (" + h + ")");

        //"Bom dia", 3, 235.13, true, -135, 256.23F, 'A', 6463275245745L.
    }
}
