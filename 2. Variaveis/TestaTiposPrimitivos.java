public class TestaTiposPrimitivos {
    public static void main(String[] args) throws Exception {
        boolean bool = true;
        System.out.println("Boolean: " + bool);

        char character = 'P';
        System.out.println("Char: " + character);

        byte byteNumber = 7;
        System.out.println("Byte: " + byteNumber);

        short shortNumber = 707;
        System.out.println("Short: " + shortNumber);

        int intNumber = 7070707;
        System.out.println("Int: " + intNumber);

        long longNumber = 707070707070707L;
        System.out.println("Long: " + longNumber);

        float floatNumber = 707.707F;
        System.out.println("Float: " + floatNumber);

        double doubleNumber = 70707.70707;
        System.out.println("Double: " + doubleNumber);
    }
}
