public class ExibeValores {
    public static void main(String[] args) throws Exception {
        byte numero1 = 19;
        System.out.println("numero1 (19): " + numero1);

        short numero2 = 1571;
        System.out.println("numero2 (1571): " + numero2);

        int numero3 = 100000;
        System.out.println("numero3 (100000): " + numero3);

        long numero4 = 2147483648L;
        System.out.println("numero4 (2147483648): " + numero4);

        float numero5 = 3.14F;
        System.out.println("numero5 (3.14): " + numero5);

        double numero6 = 3.141592653589793;
        System.out.println("numero6 (3.141592653589793): " + numero6);

        boolean bool = true;
        System.out.println("bool (true): " + bool);

        char K = 'K';
        System.out.println("char (K): " + K);
    }
}
