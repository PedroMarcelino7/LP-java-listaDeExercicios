public class OperadoresRelacionais {
    public static void main(String[] args) throws Exception {
        int a = 1;
        int b = 2;

        System.out.println(a > b); //false
        System.out.println(a >= b); //false
        System.out.println(a < b); //true
        System.out.println(a <= b); //true
        System.out.println(a == b); //false
        System.out.println(a != b); //true
    }
}
